#STRING METHODS
names = "harshad,aakruti"
relation = "loveseachother"
print(names[0:7]) # this function is used for printing string from to any index
print(len(names)) # this method is used for printing the length of string
print(relation[0:6]) # including 0 but not 6 its 6-1 = 5
print(relation[:6]) # if we skipp here index 0 then python interpreter defaultly add 0 to the index
print(names[3:9])
print(names[:])
print(names[0:-15]) # it acts as -15 as len(names)-15
print(names[-15:3]) # it acts as -15 as len(names)-15
print(names[-15:-3]) # it acts as -15 as len(names)-15 as well as -3 act as len(names)-3
